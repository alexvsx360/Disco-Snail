function removePlatformFromChunks(platform) {
  if (sewerDepth > 0) {
    for (const key in sewerChunks) {
      if (key.startsWith(sewerDepth + '_')) {
        sewerChunks[key] = sewerChunks[key].filter(item => item !== platform);
      }
    }
  } else if (typeof inWaterRealm !== 'undefined' && inWaterRealm && typeof waterChunks !== 'undefined') {
    const chunkIdx = Math.floor(platform.x / (CHUNK_WIDTH * TILE_SIZE));
    const key = 'w_' + chunkIdx;
    if (waterChunks[key]) waterChunks[key] = waterChunks[key].filter(item => item !== platform);
  } else {
    const chunkIdx = Math.floor(platform.x / (CHUNK_WIDTH * TILE_SIZE));
    const key = String(chunkIdx);
    if (chunks[key]) chunks[key] = chunks[key].filter(item => item !== platform);
  }
}

