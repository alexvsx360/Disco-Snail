/**
 * Parses a text-based map array and instantiates game objects at grid-aligned positions.
 * @param {string[]} mapArray - Each string is a row, each char is a column
 * @param {number} chunkOffsetX - World X offset for this chunk
 * @param {Object} options - { spaceStyle, groundEnemyType, flyingEnemyType }
 * @returns {Array} Array of Platform, Enemy, Coin, Pipe, BonusItem instances
 */
function buildChunkFromMap(mapArray, chunkOffsetX, options) {
  options = options || {};
  const spaceStyle = options.spaceStyle || false;
  const groundEnemyType = options.groundEnemyType || 'turtle';
  const flyingEnemyType = options.flyingEnemyType || 'fish';
  const groundY = options.groundY || GROUND_Y;
  const pitType = options.pitType || 'water';
  const items = [];
  if (options.octopusTrap && typeof octopusTrapPit !== 'undefined') octopusTrapPit = null;
  if (!mapArray || !Array.isArray(mapArray)) return items;
  const numRows = mapArray.length;
  if (numRows === 0) return items;

  for (let rowIndex = 0; rowIndex < numRows; rowIndex++) {
    let row = mapArray[rowIndex];
    if (!row) continue;
    row = (row + "0".repeat(CHUNK_WIDTH)).substring(0, CHUNK_WIDTH);
    const pixelY = groundY - (numRows - 1 - rowIndex) * TILE_SIZE;
    const isGroundRow = (rowIndex === numRows - 1);
    let groundAdded = false;
    for (let colIndex = 0; colIndex < row.length; colIndex++) {
      const ch = row[colIndex] || '0';
      const pixelX = chunkOffsetX + colIndex * TILE_SIZE;
      switch (ch) {
        case '0':
        case ' ':
          break;
        case 'W': {
          if (options.pitType !== undefined) {
            const pitY = isGroundRow ? groundY + 4 : pixelY;
            const pitH = VIEW_HEIGHT - pitY;
            const lastPit = waterPits.length > 0 ? waterPits[waterPits.length - 1] : null;
            const canMerge = lastPit && lastPit.type === pitType && lastPit.y === pitY &&
              lastPit.x + lastPit.width === pixelX;
            if (canMerge) {
              lastPit.width += TILE_SIZE;
            } else {
              const pit = new WaterPit(pixelX, pitY, TILE_SIZE, pitH, pitType);
              waterPits.push(pit);
            }
            if (options.octopusTrap && typeof octopusTrapPit !== 'undefined') {
              if (!octopusTrapPit) octopusTrapPit = { x: pixelX, y: pitY, w: TILE_SIZE, h: pitH };
              else octopusTrapPit.w += TILE_SIZE;
            }
            if (options.octopusTrap && isGroundRow) {
              const ground = new Platform(pixelX, groundY, TILE_SIZE, VIEW_HEIGHT - groundY, 'ground');
              if (options.spaceStyle) ground.spaceStyle = true;
              items.push(ground);
            }
          }
          break;
        }
        case 'G': {
          const hasGaps = /[^G]/.test(row);
          if (isGroundRow && !groundAdded && !hasGaps) {
            const ground = new Platform(chunkOffsetX, groundY, CHUNK_WIDTH * TILE_SIZE, VIEW_HEIGHT - groundY, 'ground');
            if (spaceStyle) ground.spaceStyle = true;
            items.push(ground);
            groundAdded = true;
          } else if (isGroundRow && hasGaps) {
            const ground = new Platform(pixelX, groundY, TILE_SIZE, VIEW_HEIGHT - groundY, 'ground');
            if (spaceStyle) ground.spaceStyle = true;
            items.push(ground);
          } else if (!isGroundRow) {
            const ground = new Platform(pixelX, groundY, TILE_SIZE, VIEW_HEIGHT - groundY, 'ground');
            if (spaceStyle) ground.spaceStyle = true;
            items.push(ground);
          }
          break;
        }
        case 'B': {
          const brick = new Platform(pixelX, pixelY, TILE_SIZE, TILE_SIZE, 'brick', null);
          if (spaceStyle) brick.spaceStyle = true;
          if (options.waterStyle) brick.waterStyle = true;
          items.push(brick);
          break;
        }
        case 'Q': {
          /* 'random' = roll on hit: coin/power/super/1up/disco(very rare) */
          const content = options.questionContent || 'random';
          const p = new Platform(pixelX, pixelY, TILE_SIZE, TILE_SIZE, 'question', content);
          if (spaceStyle) p.spaceStyle = true;
          if (options.waterStyle) p.waterStyle = true;
          items.push(p);
          break;
        }
        case 'E': {
          const enemyY = (rowIndex === numRows - 1) ? groundY - 32 : pixelY;
          items.push(new Enemy(pixelX, enemyY, groundEnemyType));
          break;
        }
        case 'F':
          items.push(new Enemy(pixelX, pixelY - 68, flyingEnemyType));
          break;
        case 'D':
          /* D = Question block (disco removed from map - now comes from Q randomly) */
          const qContent = options.questionContent || 'random';
          const qPlatform = new Platform(pixelX, pixelY, TILE_SIZE, TILE_SIZE, 'question', qContent);
          if (spaceStyle) qPlatform.spaceStyle = true;
          if (options.waterStyle) qPlatform.waterStyle = true;
          items.push(qPlatform);
          break;
        case 'P':
          if (options.addPipe !== false) {
            pipes.push(new Pipe(pixelX, GROUND_Y - 64, 'entrance', 1));
          }
          break;
        case 'C':
          items.push(new Coin(pixelX, pixelY));
          break;
        case 'S':
          if (typeof waterCoral !== 'undefined' && options.groundY === WATER_GROUND) {
            const coralSize = Math.floor(TILE_SIZE * 1.5);
            waterCoral.push({ x: pixelX - (coralSize - TILE_SIZE) / 2, y: pixelY - (coralSize - TILE_SIZE), w: coralSize, h: coralSize });
          }
          break;
        default:
          break;
      }
    }
  }
  return items;
}

function generateChunk(chunkIndex) {
  if (chunks[chunkIndex]) return;
  const baseX = chunkIndex * CHUNK_WIDTH * TILE_SIZE;
  const levelEndX = LEVEL_LENGTH * CHUNK_WIDTH * TILE_SIZE;
  if (baseX >= levelEndX) return;
  const chunkEndX = baseX + CHUNK_WIDTH * TILE_SIZE;
  const theme = getLevelTheme();
  const enemyTypes = theme.enemyTypes || ['turtle', 'fish'];
  const groundType = enemyTypes.find(t => !['fish', 'spider'].includes(t)) || 'turtle';
  const flyingType = enemyTypes.find(t => ['fish', 'spider'].includes(t)) || 'fish';
  const mapIndex = (chunkIndex <= 0) ? 0 : Math.floor(Math.random() * (LEVEL_CHUNK_MAPS.length - 1)) + 1;
  const mapArray = LEVEL_CHUNK_MAPS[mapIndex];
  const opts = {
    spaceStyle: false,
    groundEnemyType: groundType,
    flyingEnemyType: flyingType,
    questionContent: 'random',
    addPipe: false
  };
  if (chunkIndex === 0 && currentLevel === 1) {
    opts.pitType = 'water';
    opts.octopusTrap = true;
  }
  const items = buildChunkFromMap(mapArray, baseX, opts);
  const maxChunk = Math.floor(levelEndX / (CHUNK_WIDTH * TILE_SIZE)) - 1;
  if (sewerDepth === 0 && !inSpaceRealm && currentLevel % 3 === 0 && chunkIndex === Math.min(4 + (currentLevel % 8), maxChunk - 3) && pipes.filter(p => p.type === 'entrance').length === 0) {
    pipes.push(new Pipe(baseX + 200, GROUND_Y - 64, 'entrance', 1));
  }
  if (chunkEndX >= levelEndX) {
    if (currentLevel === 10) {
      cactusBossPortalX = levelEndX - 120;
      if (pipes.filter(p => p.type === 'cactus_boss').length === 0) {
        pipes.push(new Pipe(levelEndX - 120, GROUND_Y - 64, 'cactus_boss', 0));
      }
    } else {
      flagX = levelEndX - 80;
    }
  }
  if (sewerDepth === 0 && !inSpaceRealm && currentLevel === 2 && chunkIndex === maxChunk - 3 && celestialGateways.length === 0) {
    celestialGateways.push(new CelestialGateway(baseX + 200, GROUND_Y - 72));
  }
  chunks[chunkIndex] = items;
}

function generateSpaceChunk(chunkIndex) {
  if (chunks[chunkIndex]) return;
  const baseX = chunkIndex * CHUNK_WIDTH * TILE_SIZE;
  const levelEndX = LEVEL_LENGTH * CHUNK_WIDTH * TILE_SIZE;
  if (baseX >= levelEndX) return;
  const chunkEndX = baseX + CHUNK_WIDTH * TILE_SIZE;
  const mapIndex = (chunkIndex <= 0) ? 0 : Math.floor(Math.random() * (SPACE_CHUNK_MAPS.length - 1)) + 1;
  const mapArray = SPACE_CHUNK_MAPS[mapIndex];
  const items = buildChunkFromMap(mapArray, baseX, {
    spaceStyle: true,
    groundEnemyType: 'cyberant',
    flyingEnemyType: 'spacefly',
    questionContent: 'random',
    addPipe: false
  });
  if (spaceStage === 1 && chunkIndex === 1 && earthReturnObjects.length === 0) {
    earthReturnObjects.push({ x: baseX + 200, y: GROUND_Y - 80, width: 80, height: 80 });
  }
  if (chunkEndX >= levelEndX) {
    flagX = levelEndX - 80;
    if (spaceStage < 3) {
      spaceshipX = levelEndX - 100;
    } else {
      spaceBossPortalX = levelEndX - 120;
    }
  }
  chunks[chunkIndex] = items;
}

function generateSewerChunk(depth, chunkIndex) {
  const key = depth + '_' + chunkIndex;
  if (sewerChunks[key]) return;
  const baseX = chunkIndex * CHUNK_WIDTH * TILE_SIZE;
  const chunkEndX = baseX + CHUNK_WIDTH * TILE_SIZE;
  if (chunkEndX > SEWER_WIDTH) return;

  let pitType = 'water';
  if (depth >= 3 && depth <= 4) pitType = 'lava';
  else if (depth >= 5) pitType = 'poison';

  const types = SEWER_ENEMIES[Math.min(depth - 1, 4)];
  const groundType = types.find(t => !['fish', 'spider'].includes(t)) || 'rat';
  const flyingType = types.find(t => ['fish', 'spider'].includes(t)) || 'spider';

  const mapIndex = (chunkIndex <= 0) ? 0 : Math.floor(Math.random() * (SEWER_CHUNK_MAPS.length - 1)) + 1;
  const mapArray = SEWER_CHUNK_MAPS[mapIndex];
  const items = buildChunkFromMap(mapArray, baseX, {
    spaceStyle: false,
    groundEnemyType: groundType,
    flyingEnemyType: flyingType,
    questionContent: 'random',
    addPipe: false,
    groundY: SEWER_GROUND,
    pitType: pitType
  });

  const r = (i, max) => Math.floor(Math.random() * (max + 1));
  if (depth >= 3 && r(90, 4) === 0) {
    const spikeX = baseX + 80 + r(91, Math.max(1, CHUNK_WIDTH * TILE_SIZE - 200));
    const spikeW = 64 + r(92, 64);
    sewerSpikes.push({ x: spikeX, y: SEWER_GROUND - 16, width: spikeW, height: 16 });
  }
  if (depth >= 4 && r(95, 5) === 0) {
    const colX = baseX + 100 + r(96, Math.max(1, CHUNK_WIDTH * TILE_SIZE - 250));
    fireColumns.push({ x: colX, y: 40, vx: -0.8 - depth * 0.2, vy: 0.8 + depth * 0.15, life: 120, phase: 0 });
  }

  sewerChunks[key] = items;
}

function generateWaterChunk(chunkIndex) {
  const key = 'w_' + chunkIndex;
  if (waterChunks[key]) return;
  const baseX = chunkIndex * CHUNK_WIDTH * TILE_SIZE;
  if (baseX >= WATER_WIDTH) return;
  const wStage = typeof waterStage !== 'undefined' ? waterStage : 1;
  const stageOffset = Math.min(wStage - 1, Math.max(0, WATER_CHUNK_MAPS.length - 5));
  const mapIndex = chunkIndex <= 0 ? 0 : Math.min(chunkIndex + stageOffset, WATER_CHUNK_MAPS.length - 1);
  const mapArray = WATER_CHUNK_MAPS[mapIndex];
  const WATER_ENEMIES = [['turtle', 'fish'], ['crab', 'fish'], ['crab', 'jellyfish'], ['crab', 'jellyfish'], ['crab', 'jellyfish']];
  const enemySet = WATER_ENEMIES[Math.min(wStage - 1, 4)];
  const groundType = enemySet[0];
  const flyingType = enemySet[1];
  const items = buildChunkFromMap(mapArray, baseX, {
    spaceStyle: false,
    waterStyle: true,
    groundEnemyType: groundType,
    flyingEnemyType: flyingType,
    questionContent: 'random',
    addPipe: false,
    groundY: WATER_GROUND,
    pitType: 'water'
  });
  const coralSize = 48;
  for (let i = 0; i < 4; i++) {
    const cx = baseX + 100 + i * 180 + (chunkIndex % 3) * 40;
    if (typeof waterCoral !== 'undefined') waterCoral.push({ x: cx - (coralSize - 32) / 2, y: WATER_GROUND - coralSize, w: coralSize, h: coralSize });
  }
  if (chunkIndex > 0 && chunkIndex % 2 === 0 && typeof sludgePits !== 'undefined') {
    sludgePits.push({ x: baseX + 150, y: WATER_GROUND - 32, w: 64, h: 32, timer: 0, lobsterY: WATER_GROUND - 32, lobsterVy: 0 });
  }
  if (chunkIndex > 0 && typeof Enemy !== 'undefined') {
    const flyCount = 1 + (chunkIndex % 3);
    for (let f = 0; f < flyCount; f++) {
      const fx = baseX + 80 + f * 200 + (chunkIndex % 5) * 30;
      const fy = WATER_GROUND - 120 - (chunkIndex % 4) * 60;
      items.push(new Enemy(fx, fy - 68, flyingType));
    }
  }
  if (chunkIndex >= 4 && typeof waterBossPortalX !== 'undefined' && typeof waterStage !== 'undefined' && waterStage === 5) {
    waterBossPortalX = WATER_WIDTH - 120;
  }
  waterChunks[key] = items;
}

