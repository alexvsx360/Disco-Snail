/* levels.js - Level generation, sewer, pipes, collision, particles */

/*
 * TILE_LEGEND - Character to game entity mapping (Grid-Based Tilemap)
 * '0' or ' ' = Empty space
 * 'G' = Ground block
 * 'B' = Brick Block (Platform)
 * 'Q' = Question Block (Platform)
 * 'E' = Ground Enemy (turtle, rat, cyberant)
 * 'F' = Flying Enemy (fish, spacefly)
 * 'D' = Question block (same as Q, for backwards compatibility)
 * 'P' = Pipe
 * 'C' = Coin
 * 'W' = WaterPit (water/lava/poison - sewer maps only, use options.pitType)
 * 'S' = Seaweed/Coral (oxygen refill - water maps only)
 */

function getLevelTheme() {
  const idx = Math.min(currentLevel - 1, LEVEL_THEMES.length - 1);
  return LEVEL_THEMES[Math.max(0, idx)];
}

const MAX_PARTICLES = 100;
function addParticles(x, y, count, color, emoji) {
  if (particles.length >= MAX_PARTICLES) return;
  const toAdd = Math.min(count, MAX_PARTICLES - particles.length);
  for (let i = 0; i < toAdd; i++) {
    const p = {
      x: emoji ? x + (Math.random() - 0.5) * 20 : x,
      y,
      vx: (Math.random() - 0.5) * (emoji ? 4 : 8),
      vy: emoji ? -2 - Math.random() * 3 : -Math.random() * 6 - 2,
      life: 30,
      color: color || '#ffd700'
    };
    if (emoji) p.emoji = emoji;
    if (particles.length < MAX_PARTICLES) particles.push(p);
  }
}

function addLandingDust(x, y) {
  addParticles(x, y, 5, '#8b7355');
}

const MAX_SCORE_POPUPS = 12;
function addScorePopup(x, y, text) {
  if (scorePopups.length >= MAX_SCORE_POPUPS) scorePopups.shift();
  scorePopups.push({ x, y, text, life: 60 });
}

function getLevelPower() {
  const key = inSpaceRealm ? 'Space' + spaceStage : (sewerDepth > 0 ? 'S' + sewerDepth : 'L' + currentLevel);
  if (!levelPowers[key]) levelPowers[key] = POWER_TYPES[rand(0, POWER_TYPES.length - 1)];
  return levelPowers[key];
}

function initClouds() {
  clouds = [];
  for (let i = 0; i < 6; i++) {
    clouds.push({
      x: Math.random() * 2000 - 200,
      y: 50 + Math.random() * 120,
      size: 40 + Math.random() * 40,
      speed: 0.3 + Math.random() * 0.5
    });
  }
}

function initTrees() {
  backgroundTrees = [];
  const levelW = LEVEL_LENGTH * CHUNK_WIDTH * TILE_SIZE;
  const seed = (currentLevel || 1) * 54321;
  const r = (i, max) => Math.floor(seededRandom(seed + i * 13) * Math.max(1, max));
  const treeCount = 15 + r(0, 10);
  for (let i = 0; i < treeCount; i++) {
    const x = 100 + r(i * 7, levelW - 400);
    const scale = 1.4 + seededRandom(seed + i * 17) * 1.0;
    const variant = r(i * 11, 3);
    backgroundTrees.push({ x, scale, variant });
  }
}

