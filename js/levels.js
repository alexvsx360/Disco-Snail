function checkCollision(a, b) {
  return a.x < b.x + b.width && a.x + a.width > b.x &&
         a.y < b.y + b.height && a.y + a.height > b.y;
}

function checkPlayerDamageCollision(player, other) {
  const pad = typeof PLAYER_HIT_PAD !== 'undefined' ? PLAYER_HIT_PAD : 4;
  const px = player.x + pad, py = player.y + pad;
  const pw = player.width - 2 * pad, ph = player.height - 2 * pad;
  const ow = (other.width !== undefined && other.width !== null) ? other.width : (other.w || 0);
  const oh = (other.height !== undefined && other.height !== null) ? other.height : (other.h || 0);
  return other.x < px + pw && other.x + ow > px && other.y < py + ph && other.y + oh > py;
}

function enterSewer(targetDepth) {
  sewerDepth = targetDepth;
  savedOverworldX = player.x;
  cameraX = 0;
  targetCameraX = 0;
  player.x = 200;
  player.y = SEWER_GROUND - 95;
  player.vx = 0;
  player.vy = 0;
  player.onGround = true;
  sister.x = player.x - 90;
  sister.y = player.y;
  pipeEnterCooldown = 30;
  sewerStartTimer = 45;
  waterPits = [];
  sewerSpikes = [];
  fireColumns = [];
  generateSewerPipes(sewerDepth);
  for (let i = 0; i <= 3; i++) generateSewerChunk(sewerDepth, i);
}

function exitSewer() {
  if (sewerDepth <= 1) {
    sewerDepth = 0;
    sewerStartTimer = 0;
    player.x = savedOverworldX;
    player.y = GROUND_Y - 80;
    cameraX = targetCameraX = Math.max(0, savedOverworldX - 200);
    sister.x = player.x - 90;
    sister.y = player.y;
  } else {
    const oldDepth = sewerDepth;
    sewerDepth--;
    waterPits = [];
    sewerSpikes = [];
    fireColumns = [];
    for (const k in sewerChunks) { if (k.startsWith(oldDepth + '_')) delete sewerChunks[k]; }
    player.x = 200;
    player.y = SEWER_GROUND - 95;
    player.vx = 0;
    player.vy = 0;
    player.onGround = true;
    sister.x = player.x - 90;
    sister.y = player.y;
    sewerStartTimer = 45;
    generateSewerPipes(sewerDepth);
    for (let i = 0; i <= 3; i++) generateSewerChunk(sewerDepth, i);
  }
  pipeEnterCooldown = 30;
}
